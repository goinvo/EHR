					<h3>About the Authors</h3>
					<h5>Authors</h5>
					<div class="author">
						<p><strong>Jeff Belden MD</strong> s a family physician. He is currently a Professor of Clinical Family &amp; Community Medicine at the University of Missouri-Columbia School of Medicine, and he is also an affiliated faculty member with the Information Experience Laboratory. His current responsibilities include user training, EHR implementation, collaboration with human-computer interaction researchers from the Information Experience (IE) Lab, and participation in EHR innovation projects at the Tiger Institute, a technology collaborative between the University of Missouri and Cerner Corporation.</p>
						<p>Dr. Belden’s research interests include the visual display of quantitative clinical information at the point of care, information display in clinical notes, and tools that foster collaborative conversations between patients and healthcare providers.</p>
						<p>Dr. Belden was the Founding Chair of the HIMSS EHR Usability Task Force. He has given presentation on EHR usability at the HIMSS Annual Conference and Virtual Conferences, and at other national conferences. Dr. Belden’s past experience with photography, film-making, layout and design, typography, as well as his experience as a consultant in healthcare software design inform his approach to user-centered design.</p>
						<p>He blogs on EHR usability at <a href="www.toomanyclicks.com" target="blank">www.toomanyclicks.com</a>.</p>
					</div>
					<div class="author">
						<p><strong>Jennifer Patel</strong> is an interface designer at Involution Studios in Boston. She has a fine arts degree in designing for new media from the Rochester Institute of Technology which has allowed her to specialize in interactive design and front-end development. Jennifer has designed complex systems and software for business-critical services from big data to streamlining processes, and has architected, designed, and built decision support tools for health and healthcare. She played a leading role in developing the designs and interactive prototypes found throughout the chapters, and was responsible for the book’s production.</p>
					</div>
					<div class="author">
						<p><strong>Nathan Lowrance MA</strong> is a doctoral candidate in the School of Information Science and Learning Technology in the College of Education at the University of Missouri-Columbia. His research interests include human information seeking behavior, human-computer interaction design, and the psychology surrounding choices and biases. As a graduate assistant Lowrance worked on and led a variety of usability and health IT projects at the University of Missouri’s Information Experience Laboratory.</p>
						
					</div>
					<div class="author">
						<p><strong>Catherine Plaisant PhD</strong> is a Senior Research Scientist at the University of Maryland Institute for Advanced Computer Studies. In 1988, she joined Professor Ben Shneiderman at the Human-Computer Interaction Laboratory where she is now the Associate Director of the Research. She enjoys working with multidisciplinary teams on designing and evaluating new interface technologies that are both usable and useful. Catherine Plaisant has over 140 published papers, on subjects as diverse as information visualization, medical informatics, universal access, digital humanities, technology for families, and evaluation methodologies. With Ben Shneiderman she co-authored the 4th and 5th Editions of Designing the User Interface, one of the major books on Human-Computer Interaction.</p>
					</div>

					<div class="author">
						<p><strong>Richelle Koopman MD, MS</strong> is an Associate Professor in the Department of Family and Community Medicine at the University of Missouri-Columbia School of Medicine. In addition to being a board-certified practicing Family Physician, Dr. Koopman has a master’s degree in Clinical Research from the Medical University of South Carolina. Dr. Koopman is experienced in conducting quantitative and qualitative research, and has also undertaken additional training regarding human factors and human-computer interaction. The Agency for Healthcare Research and Quality and the American Academy of Family Physicians Foundation have awarded her funding to investigate the EHR’s role in physician-patient interactions, including health information needs, information seeking by patients with chronic diseases and clinical decision support for care of patients with diabetes. She won the MU School of Medicine’s 2010 Dorsett L. Spurgeon MD Distinguished Medical Research Award.</p>
					</div>

					<div class="author">
						<p><strong>Joi L. Moore, PhD</strong> is an Associate Division Director in the College of Education and an Associate Professor with the School of Information Science and Learning Technologies at the University of Missouri. In addition, she is a Core Faculty member in the MU Informatics Institute and an Affiliated Faculty in the Black Studies Department. Her research expertise includes designing user-centered web applications (human-computer interaction) and electronic performance support systems. She is a former President of the Training and Performance Division and the Minorities in Media Affiliate for the Association for Educational Communications and Technology (AECT). In addition, she serves on the faculty of the Information Experience Lab at the University of Missouri, which conducts usability testing projects for internal and external clients. Dr. Moore also brings her experience and expertise with user-centered design to the lab.</p>
					</div>

					<div class="author">
						<p><strong>Todd R. Johnson, PhD</strong> is a professor of biomedical informatics at the University of Texas School of Biomedical Informatics (SBMI) at Houston. He has over 20 years of experience in biomedical informatics (BMI), and he is particularly interested in clinical informatics, user-centered design for health information technology, clinical data warehousing, and the secondary use of clinical care data. With Drs. Ben Shneiderman and Catherine Plaisant of the University of Maryland he co-led the ONC-funded SHARP-C subproject to develop novel interactive visualizations that help researchers and clinicians explore and understand clinical data in ways that enhance the efficiency, quality, and safety of EHRs. He also coordinated SHARPC’s development of Safety Enhanced Design Guidelines for user-centered cognitive design of health IT. </p>
					</div>

					<div class="author">
						<p><strong>Juhan Sonin</strong> is the Creative Director of Involution Studios Boston. He is responsible for all product design and service delivery, contributes to the business management of Boston operations and participates in corporate Involution planning. Since 2008, Juhan has been lecturing at MIT on design and rapid prototyping. His experience includes design and engineering management, interaction design and user experience. His extensive human-computer interaction (HCI) expertise led to numerous award-winning, on-the-shelf products and applications. He has held positions at Apple, NCSA, MIT, MITRE and several startups.</p>
